let num1 = Number(prompt("Informe um número: "))
let num2 = Number(prompt("Informe outro número: "))

if (num1 > num2) {
    console.log("O primeiro número é maior que o segundo número.")
} else if (num1 < num2) {
    console.log("O segundo número é menor que o primeiro número.")
} else {
    console.log("Os números são iguais.")
}